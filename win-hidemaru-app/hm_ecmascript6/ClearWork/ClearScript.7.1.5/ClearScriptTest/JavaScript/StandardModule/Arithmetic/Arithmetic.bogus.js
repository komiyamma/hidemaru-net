export const Meta = import.meta;

export function BogusAdd(a, b) {
    return a + b;
}

export function BogusSubtract(a, b) {
    return a - b;
}

export function BogusMultiply(a, b) {
    return a * b;
}

export function BogusDivide(a, b) {
    return a / b;
}
